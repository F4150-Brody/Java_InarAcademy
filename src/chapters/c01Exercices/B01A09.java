package chapters.c01Exercices;

public class B01A09 {
	public static void main(String[] args) {
		double area;
		double perimeter;
		double width;
		double height;
		
		width = 4.5;
		height = 7.9;
		area = width * height;
		perimeter = (width + height) * 2;
		

		System.out.println("Area of rectangle is " + area + ".");
		System.out.println("Perimeter of rectangle is " + perimeter + ".");

	}

}
